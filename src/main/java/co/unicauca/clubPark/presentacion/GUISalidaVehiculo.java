// calcula el tiempo que estuvo un vehiculo en el parqueadero
    private void jtxtTiempoVehMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtTiempoVehMouseClicked
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyy-MM-dd H:m:s");
        int diferencia;
        int dia = 0;
        int hora = 0;
        int minuto = 0;
        try {
            Date fechaInicial = dateFormat.parse(lblHoraYFechaEntrada.getText());
            Date fechaFinal = dateFormat.parse(lblHoraYFechaSalida.getText());
            diferencia = (int) ((fechaFinal.getTime() - fechaInicial.getTime()) / 1000);

            if (diferencia > 86400) {
                dia = (int) Math.floor(diferencia / 86400);
                diferencia = diferencia - (dia * 86400);
            }
            else if (diferencia > 3600) {
                hora = (int) Math.floor(diferencia / 3600);
                diferencia = diferencia - (hora * 3600);               
            }
            else if (diferencia > 60) {
                minuto = (int) Math.floor(diferencia / 60);
                diferencia = diferencia - (minuto * 60);           
            }
            
            jtxtTiempoVeh.setText("Horas: " + hora + " " + "Minutos: "+minuto);
            
        } catch (ParseException ex) {
            //jtxtTiempo.setText("Horas:" + horas + " " + "Minutos:"+minutos);
            Logger.getLogger(GUISalidaVehiculo.class.getName()).log(Level.SEVERE, null, ex);
            jtxtTiempoVeh.setText("Horas: " + hora + " " + "Minutos: "+minuto);
        }
        
        atrHoras = hora;
        atrMinutos = minuto;

    }//GEN-LAST:event_jtxtTiempoVehMouseClicked
