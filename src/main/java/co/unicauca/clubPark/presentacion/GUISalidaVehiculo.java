//sacamos el valor a pagar del vehiculo si es carro o moto,ademas de un recargo si perdio su ficha 
    private void jtxtFTotalAPagarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtFTotalAPagarMouseClicked
        // TODO add your handling code here:
        int multa;
        int tarifaPagar;
        int valorPagar;
        String aHoras = Integer.toString(atrHoras);
        String aMinutos = Integer.toString(atrMinutos);
        GestorTarifa gestorT = new GestorTarifa();
        Tarifa tari = new Tarifa();
        if (jtxtTipoVeh.getText().equals("Carro")) {
            try {
                tari = gestorT.consultarTarifa(aHoras, aMinutos);
                System.out.println("Consulta realizada");
            } catch (Exception e) {
                System.out.println(e);
            }
        } else {
            if (jtxtTipoVeh.getText().equals("Moto")) {
                try {
                    tari = gestorT.consultarTarifaMotos(aHoras, aMinutos);
                    System.out.println("Consulta realizada");
                } catch (Exception e) {
                    System.out.println(e);
                }
            }
        }

        if (jRBPerdidaFicha.isSelected()) {
            multa = 5000;
            tarifaPagar = Integer.parseInt(tari.getValorTotal());
            valorPagar = multa + tarifaPagar;
            jtxtFTotalAPagar.setText("$" + valorPagar);
        } else {
            jtxtFTotalAPagar.setText("$" + " " + tari.getValorTotal());
        }
    }//GEN-LAST:event_jtxtFTotalAPagarMouseClicked

